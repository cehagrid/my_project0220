package com.atguigu.io.object;

import java.io.Serializable;

public class Student implements Serializable {

//    private static final long serialVersionUID = 508679453816823247L;
    // private static final long serialVersionUID = -6702974473663480764L;
    private String name;
    private Integer age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Student(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "Student{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
